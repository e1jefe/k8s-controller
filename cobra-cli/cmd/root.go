import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "k8s-cli",
	Short: "A Kubernetes cluster management CLI tool",
	Long: `A command line interface for managing Kubernetes clusters.
This tool provides commands to interact with your Kubernetes cluster,
including listing resources, getting information about workloads,
and managing deployments.

Examples:
  k8s-cli pods                   # List all pods
  k8s-cli deployments            # List all deployments
  k8s-cli services               # List all services`,
	// Uncomment the following line if your bare application
	// has an action associated with it:
	// Run: func(cmd *cobra.Command, args []string) { },
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

